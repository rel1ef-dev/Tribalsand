<center><h1>SMOKE-FREE FACILITY POLICY</h1></center>

<hr>
<h1>Policy Statement </h1>

<p>
Tribal Sand is committed to providing a healthy, clean, and sustainable environment for all guests, staff, and visitors.
In alignment with our core values of environmental conservation and guest wellbeing, all Tribal Sand properties—including 
My Amani,Maya Kobe, Enkare, Sandbox, Maya Ilai, Off Duty and Zuri—are designated as 100% smoke-free facilities.
</p>

<p>
 This policy prohibits smoking of any kind in all indoor and outdoor areas of our properties.
</p>

<hr>

<h1>Scope</h1>

<ul>
  <li>This policy applies to:</li>
  <li> All guests, visitors, contractors, and staff members</li>
  <li>All Tribal Sand properties and their grounds</li>
  <li>All forms of tobacco products including cigarettes, cigars, pipes, hookahs, and any smoke-producing devices</li>
  <li>Smokeless vaping devices are permitted</li>
  <li>All indoor spaces, outdoor areas, beaches, gardens, terraces, balconies, and common areas</li>
</ul>

<hr>


<h2> Prohibited Activities</h2>
<p>The following activities are strictly prohibited on all Tribal Sand properties:</p>
<ul>
  <li>Smoking of cigarettes, cigars, or pipes of any kind</li>
  <li>Use of hookahs, shishas, or water pipes</li>
  <li>Smoking of cannabis or any other substances</li>
  <li>Use of any electronic devices that produce visible smoke or vapor clouds</li>
  <li>Smoking in vehicles on property grounds</li>
</ul>
 
<hr>


<h2> Permitted Activities</h2>
<p>
Smokeless vaping devices that do not produce visible smoke or vapor are permitted.
</p>
 
 <hr>

<h2>Rationale</h2>
<p>
This policy is implemented for the following reasons:
</p>
<ul>
  <li> Health and Safety: Protecting guests and staff from secondhand smoke exposure </li>
  <li> Environmental Conservation: Cigarette butts are the most prevalent form of beach litter worldwide. Our coastal properties are committed to marine and beach conservation</li>
  <li> Guest Experience: Ensuring a clean, fresh environment for all guests</li>
  <li> Property Maintenance: Reducing smoke damage, odors, and cleaning costs</li>
  <li> Alignment with Global Trends: Following international best practices in sustainable hospitality</li>
</ul>

<hr>

<h2>Enforcement And Penalties</h2>
<p>
Tribal Sand takes this policy seriously. Violations will result in the following consequences:
</p>
<p><strong>First Violation:</strong></p>
<ul>
  <li>Written warning</li>
  <li>Immediate fine of <strong>Ksh 5,000</strong>, payable immediately</li>
</ul>

<hr>

<p><strong>Subsequent Violations or Refusal to Comply:</strong></p>
<ul>
  <li> Additional fine of Ksh 5,000 per violation</li>
  <li> Immediate termination of stay with forfeiture of all remaining nights</li>
  <li> No refund will be provided for unused nights</li>
  <li> Guest will be required to vacate the property within 2 hours</li> 
  <li> Guest may be prohibited from future bookings at any Tribal Sand property</li> 
</ul>

<p>The decision to terminate a stay is at the sole discretion of Tribal Sand management.</p>


<hr>


<h2>Guest Responsibilities</h2>
<p>
All guests are expected to:
</p>
<ul>
  <li> Familiarize themselves with this policy upon check-in</li>
  <li> Respect the smoke-free environment</li>
  <li> Report violations to property management</li>
  <li> Dispose of any smoking materials before arriving at the property</li> 
</ul>
<hr>


<h2> Booking Acknowledgment </h2>
<p>
By completing a reservation at any Tribal Sand property, guests acknowledge that they have read, understood, 
and agree to comply with this Smoke-Free Facility Policy. Failure to comply will result in the penalties outlined above.
</p> 

<p>
This policy is included in:
</p>
<ul>
  <li> Booking confirmation emails</li>
  <li> Pre-arrival information packets</li>
  <li> Check-in documentation</li>
  <li> Signage posted throughout the property</li> 
</ul>


<hr>

<h2>Staff Responsibilities</h2>
<p>
Tribal Sand staff members are responsible for:
</p>
  
<ul>
  <li> Informing guests of this policy during the booking and check-in process</li>
  <li> Monitoring compliance throughout the property</li>
  <li> Documenting violations</li>
  <li> Enforcing penalties consistently and professionally</li> 
  <li> Reporting repeat offenders to management</li> 
</ul>

<hr>


<h2>Signage</h2>
<p>
Clear signage indicating the smoke-free policy will be prominently displayed at:
</p>
  
<ul>
  <li> Property entrances</li>
  <li> Reception/check-in areas</li>
  <li> All guest accommodations</li>
  <li> Beach access points</li> 
  <li> Outdoor seating areas</li> 
  <li> Common areas and gathering spaces</li>
</ul>

<hr>

<h2>Exceptions</h2>
<p>
There are no exceptions to this policy. All guests, regardless of length of stay or booking value, are subject to these rules.
</p>
 
<hr>



<h2>Policy Review</h2>
<p>
This policy will be reviewed annually and updated as needed to reflect best practices in sustainable hospitality and guest satisfaction.
</p>

  
