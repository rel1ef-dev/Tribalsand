                return item.category === currentCategory;
            });

            currentImageIndex = (currentImageIndex + 1) % filteredImages.length;
            showImage(currentImageIndex);
        });

        // Add scroll event listener
        window.addEventListener('scroll', handleScroll);

        // Initialize gallery
        renderGallery();
