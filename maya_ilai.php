        function renderGallery(append = false) {
            if (append) {
                loaderContainer.style.display = 'block';
                // Add a small delay to show the loading animation
                setTimeout(() => {
                    renderImages(append);
                    loaderContainer.style.display = 'none';
                }, 800);
            } else {
                renderImages(append);
            }
